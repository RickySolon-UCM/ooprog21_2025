/**
 * This is the Comments class.
 * It demonstrates different types of comments in Java.
 */
public class Comments {
    
    public static void main(String[] args) {
        
        // Single-line Comment: Prints a message to the console
        System.out.println("Hello, World!"); 
        
        /*
         * Multi-line Comment:
         * The next line will display a simple message.
         * It shows how multi-line comments work.
         */
        System.out.println("This is a multi-line comment example.");
    }
}
